//STRING METHODS
print("COUNT OF STR : \(str.count)")            //.count 
print("LOWERED STR : \(str.lowercased())")      //to lower case 
print("UPPERED STR : \(str.uppercased())")      //to upper case
print("HAS PREFIX : \(str.hasPrefix("H"))")     //hasPrefix()
print("HAS SUFFIX : \(str.hasSuffix("D"))")     //hasSuffix()

