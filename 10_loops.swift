//FOR LOOP
let interns = ["GAJINDER","DIVAY","VAANI","AJAY"]
for intern in interns{
    print(intern);
}

// FOR IN RANGE
for i in 1...10 {
    print("2 X \(i) = \(2*i)")
}

//WHILE LOOP
var i = 1;
while(i<11){
    print("3 X \(i) = \(3*i)")
    i += 1
}

//REPEAT WHILE LOOP
var i = 0
repeat{
    print(i)
    i += 1
}
while(i<15)